<div class="p"><h2>Chapter 4. Interview Vol.4 @Poland</h2></div>

<div class="p"><i>originally published on 2009-Oct-29</i></div>

<div class="p">In last time, KOKIA talked about her visiting in Ireland. The next country was Poland. After a long moving in the car, with some anxiety, they landed in the country for the first time. There they got an unexpected welcome, and the show was in Blue Note.</div>

<div class="p"><img src="http://www.cdjournal.com/image/special/20090917/4/artist1.jpg" alt="" /></div>

<div class="p">Moving by a van.</div>

<div class="p">-- The third country in the Europe tour was Poland.</div>

<div class="p">-- After Ireland, we took the flight in Berlin, got on a very big van, which was the very best thing during the tour, and thus we could stay all the way together. We went across several countries from Germany to Poland, but no one was telling us "here it is the border", and we also did not get stamped for the entry... we went in Poland without a real feeling (laugh).</div>

<div class="p">-- But, it would also be exhausting to be in a car for long time.</div>

<div class="p">-- I thought it would be so before, but it was not true. I had a very happy time. Unlike highways in Japan, there were no walls, and we could see the expanding horizon. We enjoyed the field, the scenery, and the buildings there, and we were relaxed during the moving. The weather was also pretty good, I even got some random melodies and pieces of lyrics. Moreover, I made a to-do list when I go back to Japan.</div>

<div class="p">-- The scenery must be very beautiful.</div>

<div class="p">-- Definitely. I got a lot of interesting experiences. We were communicating in English just now, but we had to use only German when we entered into Germany. The washroom systems in different countries are also different. "Hmm, that's interesting..." Like this, I was never bored during the moving. However, the time being in Poland was too short. Seemed it only contained "have a live, and go to sleep". To be honest, we hardly see anything in that country. We went to the live house directly, and after the live, we went to the hotel. In the nest morning, we were departing again. It was just too short, we wish we could have more time to visit somewhere, but we just went to the next country.</div>

<div class="p">-- I see. Well, how about the live in Poland?</div>

<div class="p">-- I was very anxious before going to the live house. It was my first time to visit this country, would there be a lot of people? I was surrounded by a calm and pastoral atmosphere, but I was very surprised when being in the live house. "In such a wonderful place, how nice the live house is!"</div>

<div class="p"><img src="http://www.cdjournal.com/image/special/20090917/4/artist2.jpg" alt="" /><br />
A castle-like live house.</div>

<div class="p">-- So Poland's Blue Note was a beautiful live house.</div>

<div class="p">-- Yes, very. It was like being originated from a real castle. Just... the dressing room was very cold. There was wind blowing into, and I just felt cold throughly. And we were talking like "is this place for storing wine? Or is this place really a dungeon?" (laugh)</div>

<div class="p"><img src="http://www.cdjournal.com/image/special/20090917/4/artist4.jpg" alt="" /></div>

<div class="p">Being with fans.</div>

<div class="p">-- But the live itself was just excellent. We can feel that from the video.</div>

<div class="p">-- Yeah, it may be the best one among the whole tour. First, everyone was so open-minded. Although sometimes, it did not set up well, the Polish audience gave me a feeling of that they were saying "Uwa, that's KOKIA! We're waiting for you!!" Their response has reached my heart. I was very happy, and thus got very excited. There were also a lot of people there standing all the time.</div>

<div class="p"><img src="http://www.cdjournal.com/image/special/20090917/4/artist3.jpg" alt="" /><br />
The audience's standing ovation!!</div>

<div class="p"><img src="http://www.cdjournal.com/image/special/20090917/4/artist5.jpg" alt="" /></div>

<div class="p">At the side stage.</div>

<div class="p">-- The hearty applause and cheering were also a good communication between you and the audience.</div>

<div class="p">-- And the distance between the audience and me was perfect. I could feel them, and they could also see my performance. That was such a nice position, especially for the first stage, there must be a lot of people wondering what kind of a person I am. I think by looking at one's face, others can get the one's personality. Therefore, it was a very good place, which could make you feeling being at home. Moreover, maybe I would rather to say that I so fortunate for not understanding Polish at all. I only know a little French and German, so I was always telling myself do not say weird things, and as a result, sometimes I was way too nervous. Yet, since I do not understand Polish, I could just sing with my heart, and I was in a doing well all the way (laugh).</div>

<div class="p">-- We saw that from the DVD, that you said "KOKIA-chan, I am doing well" (laugh). It was so nice to get a warm welcome in a country you first visited. It was also nice to get your music into people's heart...</div>

<div class="p">-- I think the Internet did a major work for making more people knowing about me. There were also people use my CD as a gift. Another thing impressed me was that everyone was cheerful. The atmosphere on a live can indicate the nationality, I think I got it a lot -- People in Poland are cheerful. During the autograph session, everyone was like "you also like KOKIA?" and thus could go well everywhere. Also, that Polish females were very pretty was another impression for my visiting in Poland.</div>

<div class="p"><i>Reporter: Tomoyuki Mori (2009-Oct.)</i></div>

