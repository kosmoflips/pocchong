<div class="p"><b>- About the Title in Romaji:</b><br />
Even though the song title should be literally romanized as "Chyouwa", most people choose to use "Cyouwa" as its romanized title. Because, if you look at the lyrics, there is a number serial: "3 25 15 21 23 1". At first, I was wondering why those numbers and what they mean, yet there's nothing secret. If you write down the letters with the rule of writing "A" as "1", "B" as "2", ... "Z" as "26", you'll see this number serial becoming "C-Y-O-U-W-A".</div>

