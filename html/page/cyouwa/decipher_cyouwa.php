<h2>Decipher Cyouwa &amp; Nightingale</h2>
<div class="p">KOKIA-words were used a lot in this two songs (and a few others, but there are no official lyrics of them ^^;).</div>

<div class="p" style="color:#990000"><b>Important: "gematria" is basically sung in KOKIA-words, yet the rule used in writing the lyrics was completely <a href="/cyouwa/decipher_gematria.html">another story</a>.</b></div>

<hr />

<div class="p">in "Cyouwa" and "Follow the Nightingale", KOKIA used a "simple" rule to make KOKIA-words:<br />

<b>Reading reversely.</b></div>

<div class="p">e.g.<br />
<blockquote>
<i>regular text: watashi-ha(wa)-koko-ni-iru (私はここにいる / I am here)</i>
<i>in KOKIA-words: rui-ni-koko-ha-shitawa</i></blockquote>
</div>

<div class="p">This is just the basic rule. Sometimes, you need to reverse phrase by phrase, yet sometimes, you need to reverse sentence by sentence, and sometimes, that's paragraph by paragraph. Also, there are different ways to "separate" the reversed KOKIA-words (you'll see how those were applied below).</div>

<div class="p">Make sense? ^^</div>

<hr />

