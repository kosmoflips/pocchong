<div class="p">* From the booklet, there are arrows to indicate the reading direction and the "translated" lyrics was also shown there. Yet that's not a literal translation, it didn't explain the extra words.</div>

<div class="p"><b>original KOKIA-words:</b><br />
nimiunooto denzushi denzu sea denzu sea</div>

<div class="p"><b>translated version:</b><br />
oto no umi ni shizunde shizunde</div>

<div class="p"><b>unmatched words:</b><br />
nimiunooto denzushi denzu sea denzu sea</div>

<div class="p"><b>my thinking:</b><br />
the lyrics is about the sea of sound, thus the "denzu sea denzu sea" part is probably the word-effect of expressing the sound of wave. So, I think, if translate it literally, it could be like that:<br />
"oto no umi ni shizunde zunde a~se(shi)zunde a~se(shi)... " which is like the echo of waves.</div>

<div class="p">** The original version, which was in album "trip trip", doesn't have the last phrase, which is "Kekkai ha(wa) yaburareta, tsuki no kami, ryuu ga naiteiru / 決壊は破られた　月の神　龍が泣いている". This phrase appears in the ~with reflection~ version, and if you only see the lyrics from the booklet, you'll probably not find where KOKIA is singing it. However, you might have noticed the background chorus is singing in an Italian-like language. That's this phrase, which was written in Japanese from the booklet, yet was sung in KOKIA-words actually.</div>

