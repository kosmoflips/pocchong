<h2>KOKIA and Chopin and Painting</h2>

<div class="p"><i>source: KOKIA blog "2010年05月04日 - ショパンの調べ"</i><br />
*below: partly translated from KOKIA's blog*</div>

<div class="p">I went to a concert.<br />
That was Yukio Yokoyama (横山幸雄)'s playing-Chopin's-166-pieces concert.</div>

<div class="p">I felt very surprised about the concert. (...) This concert had four sections, Yokoyama would play from 9:00 until 26:00 (equals 2AM). He was also going to make a new record in the Guinness book. Not only the player, to concentrate was not that easy even for the audience.</div>

<div class="p">I attened the section two, from 13:00 to 16:30, 63 pieces in all.<br />
I was very glad to have the chance for the concert; even though just to listen to CD recordings, to listen to 63 Chopin pieces in a row isn't easy either, I think.</div>

<div class="p">(...)</div>

<div class="p">Chopin's music is like impressionism paintings for me.<br />
I especially Monet and works which have similar colouring style.<br />
That is very romantic, and the ethereal and a bit painful melody is just like an imaged scenery.<br />
I felt like the sceneries from his mind were extended along the staff.</div>

<div class="p">Although Chopin was a typical musician in romanticist era, and it was earlier than the impressionist era, was it possible that artists from the next era listened to his beautiful melody, and thus left us so many beautiful paints?<br />
Monet, Cézanne, Degas, Gogh... all of them had "trembling" paintings.</div>

<div class="p">Not only paintings, artists after Chopin also include my favorited artists like Ravel and Debussy.<br />
Little by little, people's thoughts were written based on earlier people, which is very surprising for me.</div>

<div class="p">Chopin's music is beautiful, as well as Yokoyama's performance. Very delicate and dynamic.</div>

<div class="p">In June, when I go to Paris, I'll see a lot of paintings, and I want to try to make some music based on them.<br />
That will be the harmony of colours that I get from Monet, Degas and others.</div>

