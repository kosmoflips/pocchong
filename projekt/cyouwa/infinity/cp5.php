　「1,200kmくらいだったのかな？　たしか13時間くらいかかったんですけど、ちゃんと移動日があったから、それほど大変ではなかったですね。私としては、アイルランドからポーランドに行って、そのまま本番で歌ったときのほうが体力的に大変でした。移動日があるとちゃんとノドを休められるし、車から見える風景も綺麗だったので。もうすぐ、このツアーも終わっていくんだなって思いながら窓の外の風景にふけっていました」<br />
――ベルギーの街並もホントに綺麗で。
　「ベルギーは２回目だったんですけど、コンサート会場の周辺は前回も歩いていたから、けっこう覚えていましたね。私が歩いたのは会場の周辺だけですけど、有名な小便小僧だったり、王宮の広場だった場所にも昨年に引き続き足を運ぶことができて、懐かしく、散歩できました。街並は綺麗で可愛らしいし、夜歩いていてもわりと安全そうに感じましたし、なんといってもワッルフやチョコレートはとびきり美味しいですし、日本人にとてもなじみやすい場所だと思いますよ。ちょこちょこ路地に入っていくのも楽しいし」<br />
――ワッフル、食べてましたよね。
　「はい（笑）。お店によってアレンジが違うんですよねぇ。DVDの中で私が食べていたあれはわりとオーソドックスなやつだと思います。フルーツとかのっていなかったので。美味しいんですよ、ホントに。ベルギーのコンサートを楽しみにしていたいくつかの理由のうちの１つであったことは間違いないです」</div>

