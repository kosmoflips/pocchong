<div class="p">――ポーランドからベルギーまでの移動はどうでした？<br />
<img src="http://www.cdjournal.com/image/special/20090917/5/artist1.jpg" alt="" /><br />
やっとの思いでベルギー到着の夜。<br />
<img src="http://www.cdjournal.com/image/special/20090917/5/artist3.jpg" alt="" /><br />
