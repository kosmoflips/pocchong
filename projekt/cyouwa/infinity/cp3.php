<div class="p"><h2>Chapter 3. Interview Vol.3 @Ireland</h2></div>

<div class="p"><font size="4"><font size="4">KOKIA talked about lives in France in the last time. The next country she visited after France was Ireland. She began rehearsal soon after their arrival in Dublin. Such schedule sounded a bit forceful, and for herself, it seemed that lives in there were with more anxiety…<br />
<img src="http://www.cdjournal.com/image/special/20090917/3/live1.jpg" alt="" /></div>

<div class="p">The big poster in front of the live location.</div>

<div class="p">-- The second country was Ireland.</div>

<div class="p">-- At first, we moved to there (laugh). We went in the early morning, and began rehearsal soon after the arrival, as tomorrow would be a real show.</div>

<div class="p">-- That was rather a forceful schedule. Were you going to meet the Ireland band, Puck Fair?</div>

<div class="p">-- Yes. We began the first rehearsal with them in the banquet room of the hotel -- It was almost impossible to begin rehearsal with a band I'd never met right before the live day (laugh). I'd rather to say all those things during the tour were a kind of challenge than an exciting experience. But I had to do all things I needed to do.</div>

<div class="p">-- What about the response of the rehearsal?</div>

<div class="p">-- In fact, I had pretty much of anxiety when I started the rehearsal. But you know, the more anxiety you have, the result could be even worse. "This is just normal to have lives oversea." That was what I thought.</div>

<div class="p">-- I see, so this was very tough. Then, what about in the live house?</div>

<div class="p">-- It was a live house in Temple Bar, which is the most popular place in Dublin. It was newer than other places, and I thought it was a hot spot there. Many rock bands also have lives there.</div>

<div class="p"><img src="http://www.cdjournal.com/image/special/20090917/3/live2.jpg" alt="" /></div>

<div class="p">With band members of Puck Fair.</div>

<div class="p">-- So the audience was in rather an exciting situation, right?</div>

<div class="p">-- Yeah. I enjoyed that moment a lot. As for the content of the live, it was very different from those at other places. Not a "KOKIA one-man live", it was more like a live of Puck Fair with the guest KOKIA. There were also fans of them who attended the live, and I felt like "I'm KOKIA, nice to meet you" at that time. They also played some traditional and their own music on that live.</div>

<div class="p">-- What did you feel about the Irelish traditional music played in the very location?</div>

<div class="p">-- I was happy, very. The approach was rather different, of both harmonization and melody. I was surprised sometimes about "wah, the melody could expend in that way". That could be a kind of music that's more flexible on stage, and that was very interesting.</div>

<div class="p">-- So you must have got experiences that you'd never had in Japan.</div>

<div class="p">-- In Japan, I can image what would happen. Things like what happened in Ireland were rare. In most cases, things just went to be like what I thought. It's not about if I'm good at imaging the real situation, I just can feel what will be going on.</div>

<div class="p">-- Yet, it's not very true that most people can be familiar with working together with an Irish band.</div>

<div class="p">-- I'll be fine wherever I am, I think (laugh). But it would also be a pain if I couldn't handle things well as a musician.</div>

<div class="p"><img src="http://www.cdjournal.com/image/special/20090917/3/live5.jpg" alt="" /><br />
Attracting the audience in Ireland.</div>

<div class="p">-- You really like Ireland. You've ever said "I just thought it up suddenly, so I went to Ireland".</div>

<div class="p">-- Isn't it an old story long ago (laugh)? I'm always asked "do you like Irish music?" from a lot of interviews. That could be because when I felt unsure about what to choose one between "India and Ireland" as my travel destination, I chose Ireland at last, and told everyone "because I was asked more about Ireland from you, I'll go there."</div>

<div class="p">-- Some of your works have the influence from Irish traditional music.</div>

<div class="p">-- Although I always hear comments like this, I'm not very sure about it now. Like "what's real Irish music?" I understood when I really went to Ireland, this is very difficult. I mean, those kinds of rhythm and melody were inherited in their blood. Those music can be rather intense actually. That's not a type you can learn easily.</div>

<div class="p"><img src="http://www.cdjournal.com/image/special/20090917/3/live4.jpg" alt="" /></div>

<div class="p">The impressive moment!</div>

<div class="p">-- I see. Then, what else gave you strong impression except the concerts?</div>

<div class="p">-- The location of the hotel in Ireland was superb! It was the best beach in front of me, and that was my praise at the very moment. Also, I had a lot of fun with the bath (laugh).</div>

<div class="p">-- You didn't enjoy the bath in Paris a lot?</div>

<div class="p">-- That was true. Bath is a very important point of adjusting myself into a good condition. I asked to a room with bathtub before I started the tour. But even though that is a absolutely necessary point for me to have the best performance on stage, in fact, things like "there's no hot water" or "there's no a plug" always happened (laugh). That would be kind of bad memories from the tour.</div>

<div class="p">-- Interesting. So there was only a "bathtub-like" thing?</div>

<div class="p">-- Maybe. As a result, I didn't enjoy the bath during the three days in Paris. The first time to enjoyed the bath thoroughly began from the day in Ireland. Well, after that there was still a long trip to go though… Yet as a female, it's very necessary to keep neat all the time. That to be the exact "KOKIA" not only on stage is always in my mind.</div>

<div class="p">-- Not only on stage?</div>

<div class="p">-- Right. For example, if we gather at 7AM, I will prepare from 5AM. It's not only to be in front of others after applying the makeup, also include taking a bath, putting on neat clothing, and keeping a good condition. Because those things are all done in the very beginning of a day, it's necessary for me to give a good performance on stage, I think. However, since during the tour, the time that can't without KOKIA became much longer, such things were more important than normal.</div>

