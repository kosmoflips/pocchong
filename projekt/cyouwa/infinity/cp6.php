<div class="p">――ついに今回のツアーの最終の地、ドイツです。
　「ドイツもポーランドと同じで、到着した日にすぐライヴだったんですよ。ドキュメントのDVDのなかでも言ってるんですけど、会場の感じが昔私がデビューしたての頃に出演していた小さめのライヴ・ハウス、ルイード（原宿）やEgg man（渋谷）にちょっと似ていたんですよ」<br />
――いわゆるライヴ・ハウスですよね。
　「しかも、ロックな雰囲気の（笑）。だからなんとなく、デビュー当時のことを思い出して懐かい気持ちになりました。最近はプライベートでもそういった場所にあまり立ち入らなくなってしまったこともあって、ドイツのライヴは最近の私のステージよりも、場所の雰囲気が違うこともあって、ちょっとテンションあがりました」<br />
――ワイルドな雰囲気の会場ですよね、全体的に。10代の若者がロック・バンドの演奏で暴れまくってるような。
　「そうなんですよ。ロック・バンドがやっているイメージが強い場所でした。最初は'そう来たか！'って感じで、久しぶりのスタンディングのライヴに、少し戸惑ったのですけど、会場に合わせた歌を歌うことだったり、会場に合わせたテンションに持っていくっていうのは、いつもと変わりがないので。今回のツアーも、すべての会場で少しずつ、内容や歌い方も変えましたからね」</div>

