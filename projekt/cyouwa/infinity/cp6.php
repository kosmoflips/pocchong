<div class="p">――ドイツの場合は、どんなテンションだったんですか？
　「うーん、言葉にするのは難しいんですけど……。一つエピソードを紹介すると、予定していたステージで着るはずだった洋服を会場入りして雰囲気を見てから変えたんですよ。もう少しエレガントで、'衣装感'のある洋服を用意していたんですけど、会場だったり、来てくれているお客さんの様子、雰囲気を見て、'これはちょっと違うな'と思って。私、ライヴが始まる前に、ステージの袖から客席を見ることが多いんですよ。それはただの興味本位ではなくて、自分なりのリサーチなんですよね。今日のお客さんは若いな、とか、どんなものを持ってるのかな？とか」<br />
――そこから自ずと、その日のライヴの雰囲気が決まっていく。
　「その日のKOKIAを決める、という感じですね。あとね、ステージに出ていくときのテンションもすごく大事なんです。その瞬間にお客さんをつかめるかどうか、ほぼ決まってしまうこともあるので……。ドイツのときは、'ようこそ'というよりは、'やあ！'っていう感じで出ていったほうがいいな、と。オーチャードホールでやっているような感じではなく、こっちから近づいていくっていうか、距離を縮めて親近感を持ってもらえるようにしたほうがいい。だから衣装も、エレガントなデザインのものではなくて、もっとカジュアルなものに変えたんですよ。スタッフにお願いして、ホテルから持ってきてもらって」</div>

