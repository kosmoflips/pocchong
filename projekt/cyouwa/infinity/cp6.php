<div class="p">――オーディエンスのリアクションはどうでした？
　「お国柄もあったのかなぁ？みんな好きなように楽しんでくれていたように思います。日本での私のコンサートでは考えられないことだけれど、モニターにもたれて座っている人も居たし、スタンディングでステージとお客さんの最前列の方の境目がゆるかったのもあるのですけど、なぜかステージにくい込んで座っているお客さんが居たり。'ステージの内側にいるより、客席に居たほうが外音のいい音がよく聴こえるのにな'なんて思いながら（笑）。私も楽しかったですね。ツアー最後のステージっていうのもあったので、私自身'今日は何でもアリだね！'みたいな気持ちになっていのかもしれません」<br />
――ホッとした気持ちもありました？　無事に終わった！ っていう。
　「これもDVDのなかで話していたことなんですけど、実は達成感は後からじんわり来たけれど、その日はそれほど特別な気持ちにはなれなかったんですよね。スポーツ選手が毎日のように試合をするみたいに、私もツアーに身体が慣れてきていたんですねきっと。移動して、リハーサルして、ライヴっていうサイクルが馴染んできて、'まだ行けるよ'っていう感じもあって。同じ内容のライヴを続けていたらマンネリを感じることもあるのかもしれないけど、今回のツアーは全部違ってましたからね。国も違うし、セット・リストも違うし。いい意味で、緊張感は持続できていたと思います」<br />
――なるほど。ドイツという国の印象は？
　「非常に好印象でした！　その判断基準はね、お手洗いがとにかくキレイだったんです。ホテルに泊まる、水を飲む、お手洗いに行くっていうのはどこの国でもするじゃないですか。他の国ではお手洗いに悩まされることもあったので。お手洗いがキレイなドイツには本当に感激。ホントにね、高速道路の国境を越えた瞬間にトイレがキレイになるんですよ。ほんの少ししか離れていないところでも、ドイツに入った途端に。私が足を運んだところが偶然だったのか、ドイツは食周りも衛生的だったし、過ごしやすかったですね。そう、しかもね、今回のツアー中、ずっと悩まされ続けていた、洗面周りでお湯が出ない！問題。ドイツはお湯がジャンジャン出たんですよ！　疲れた体を温かいお湯でいっぱいになったバスタブ入れることができた時は、'これはツアーをやり遂げたことのご褒美？'って思いました。できれば、もっと早くご褒美が欲しかったんですけど（笑）」<br />
取材・文／森 朋之（2009年10月）</div>

